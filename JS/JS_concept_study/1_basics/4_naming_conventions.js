/* Naming Convention 1 */
/**
 * 변수 이름 지을 때
 * 1) 일반적으로 영어(라틴문자)를 사용하며 문자와 숫자 모두 사용 가능
 * 2) 특수 기호는 언더 스코어(_)와 달러($)를 사용 가능
 * 3) 숫자로 이름 시작 불가능 
 *    Ex> 1Name, 2Hello
 * 4) 키워드는 변수명으로 사용할 수 없다.
 *    Ex> var const 'var';
 */

let jisoo = '지수';
var $jisoo = '$지수';
const _jisoo = '_지수';

